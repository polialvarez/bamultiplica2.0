// string --> texto
// numerico --> numero
// buleano --> verdadero o falso

// Operador de igualdad simple
console.log(40 == '40')

// Operador de igualdad simple con buleanos
console.log(0 == false)

// Operador de comparación estricta
console.log(40 === '40')

// Operador de comparación estricta con buleanos
console.log(1 === true)

// Operador de desigualdad simple
console.log((15 != '15'))

// Operador de desigualdad estricta
console.log(15 !== '15')

// Operadores de comparación mayor que
console.log(15 > 15)

// Operadores de comparación mayor o igual que
console.log(15 >= 15)

// Operadores de comparación menor que
console.log(15 < 15)

// Operadores de comparación menor o igual que
console.log(15 <= 15)

// Operadores lógicos
let numero1 = 1
let numero2 = 2
let texto1 = '1'

// Operador && requiere que todas las comparaciones sean true
console.log(numero1 == texto1 && numero1 == numero2)

// Operador || requiere que al menos una de las comparaciones sean true
console.log(numero1 == texto1 || numero1 == numero2)

// Operador ! evalúa si tu variable o dato es falso 
let buleano = false

console.log(!buleano)

console.log(buleano)
